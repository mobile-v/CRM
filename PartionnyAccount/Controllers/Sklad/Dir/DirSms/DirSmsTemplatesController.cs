        // GET: api/DirSmsTemplates
        public async Task<IHttpActionResult> GetDirSmsTemplates(HttpRequestMessage request)
        {
            try
            {
                #region Проверяем Логин и Пароль + Изменяем строку соединения + Права + Разные Функции

                //Получаем Куку
                System.Web.HttpCookie authCookie = System.Web.HttpContext.Current.Request.Cookies["CookieIPOL"];

                // Проверяем Логин и Пароль
                Classes.Account.Login.Field field = await Task.Run(() => login.Return(authCookie, true));
                if (!field.Access) return Ok(returnServer.Return(false, Classes.Language.Sklad.Language.msg10));

                //Изменяем строку соединения
                db = new DbConnectionSklad(connectionString.Return(field.DirCustomersID, null, true));

                //Права (1 - Write, 2 - Read, 3 - No Access)
                /*
                int iRight = await Task.Run(() => accessRight.Access(connectionString.Return(field.DirCustomersID, null, true), field.DirEmployeeID, "RightDirSmsTemplates"));
                if (iRight == 3) return Ok(returnServer.Return(false, Classes.Language.Sklad.Language.msg57(0)));
                */

                //Разные Функции
                function.NumberDecimalSeparator();

                //Получам настройки
                Models.Sklad.Sys.SysSetting sysSetting = await db.SysSettings.FindAsync(1);

                #endregion


                #region Параметры

                Params _params = new Params();

                //paramList -список параметров
                var paramList = request.GetQueryNameValuePairs();
                //Параметры
                _params.limit = 999999; // sysSetting.PageSizeDir; //Convert.ToInt32(paramList.FirstOrDefault(kv => string.Compare(kv.Key, "limit", true) == 0).Value); //Записей на страницу
                _params.page = Convert.ToInt32(paramList.FirstOrDefault(kv => string.Compare(kv.Key, "page", true) == 0).Value);   //Номер страницы
                _params.Skip = _params.limit * (_params.page - 1);
                _params.type = paramList.FirstOrDefault(kv => string.Compare(kv.Key, "type", true) == 0).Value;
                _params.parSearch = paramList.FirstOrDefault(kv => string.Compare(kv.Key, "parSearch", true) == 0).Value; if (_params.parSearch != null) _params.parSearch = _params.parSearch.ToLower(); //Поиск
                _params.MenuID = Convert.ToInt32(paramList.FirstOrDefault(kv => string.Compare(kv.Key, "MenuID", true) == 0).Value);   //Номер страницы

                _params.DirSmsTemplateTypeS = Convert.ToInt32(paramList.FirstOrDefault(kv => string.Compare(kv.Key, "DirSmsTemplateTypeS", true) == 0).Value);   //Номер страницы
                _params.DirSmsTemplateTypePo = Convert.ToInt32(paramList.FirstOrDefault(kv => string.Compare(kv.Key, "DirSmsTemplateTypePo", true) == 0).Value);   //Номер страницы

                #endregion


                if (_params.type == "Grid")
                {
                    #region Основной запрос *** *** ***

                    var query =
                        (
                            from dirSmsTemplates in db.DirSmsTemplates
                            where dirSmsTemplates.MenuID == _params.MenuID
                            select new
                            {
                                DirSmsTemplateID = dirSmsTemplates.DirSmsTemplateID,
                                DirSmsTemplateName = dirSmsTemplates.DirSmsTemplateName,
                                DirSmsTemplateMsg = dirSmsTemplates.DirSmsTemplateMsg,
                                DirSmsTemplateType = dirSmsTemplates.DirSmsTemplateType,
                            }
                        );

                    #endregion


                    #region Условия (параметры) *** *** ***


                    #region Не показывать удалённые

                    /*if (!Convert.ToBoolean(sysSetting.DeletedRecordsShow))
                    {
                        query = query.Where(x => x.Del == sysSetting.DeletedRecordsShow);
                    }*/

                    #endregion


                    #region Фильтр DirSmsTemplateType S и Po

                    if (_params.DirSmsTemplateTypeS != _params.DirSmsTemplateTypePo)
                    {
                        if (_params.DirSmsTemplateTypeS > 0) query = query.Where(x => x.DirSmsTemplateType >= _params.DirSmsTemplateTypeS);
                        if (_params.DirSmsTemplateTypePo > 0) query = query.Where(x => x.DirSmsTemplateType <= _params.DirSmsTemplateTypePo);
                    }
                    else
                    {
                        if (_params.DirSmsTemplateTypeS > 0) query = query.Where(x => x.DirSmsTemplateType == _params.DirSmsTemplateTypeS);
                    }

                    #endregion


                    #region Поиск

                    if (!String.IsNullOrEmpty(_params.parSearch))
                    {
                        //Проверяем число ли это
                        Int32 iNumber32;
                        bool bResult32 = Int32.TryParse(_params.parSearch, out iNumber32);


                        //Если число, то задействуем в поиске и числовые поля (_params.parSearch == iNumber)
                        if (bResult32)
                        {
                            query = query.Where(x => x.DirSmsTemplateID == iNumber32 || x.DirSmsTemplateName.Contains(_params.parSearch));
                        }
                        else
                        {
                            query = query.Where(x => x.DirSmsTemplateName.Contains(_params.parSearch));
                        }
                    }

                    #endregion


                    #region OrderBy и Лимит

                    query = query.OrderBy(x => x.DirSmsTemplateName).Skip(_params.Skip).Take(_params.limit);

                    #endregion


                    #endregion


                    #region Отправка JSON

                    //К-во Номенклатуры
                    int dirCount = await Task.Run(() => db.DirSmsTemplates.Count());

                    //А вдруг к-во меньше Лимита, тогда показать не общее к-во, а реальное!
                    int dirCount2 = query.Count();
                    if (dirCount2 < _params.limit) dirCount = _params.limit * (_params.page - 1) + dirCount2;

                    dynamic collectionWrapper = new
                    {
                        sucess = true,
                        total = dirCount,
                        DirSmsTemplate = query
                    };
                    return await Task.Run(() => Ok(collectionWrapper));

                    #endregion
                }
                else //Tree
                {
                    #region Отобразить только "Руты" *** *** ***

                    var query =
                        (
                         from x in db.DirSmsTemplates
                         where x.MenuID == _params.MenuID
                         select new
                         {
                             id = x.DirSmsTemplateID,
                             text = x.DirSmsTemplateName,
                             leaf = true
                         }
                        );

                    #endregion


                    #region Отправка JSON

                    //return Ok(await Task.Run(() => query));

                    dynamic collectionWrapper = new
                    {
                        query
                    };
                    return Ok(await Task.Run(() => collectionWrapper));

                    #endregion
                }
            }
            catch (Exception ex)
            {
                return Ok(returnServer.Return(false, exceptionEntry.Return(ex)));
            }
        }

